def _kpi(
    kpi_id: str,
    kpi_name: str,
    group: str,
    definition: str,
    formula: str,
    unit: str,
    level: str,
    visual_type: str,
    priority: str,
    dashboard_page: str,
    drilldown_behavior: str,
    drilldown_filter_preset: Dict[str, Any],
    api_endpoint: str = "/api/kpis/all-values",
) -> Dict[str, Any]:
    return {
        "kpi_id": kpi_id,
        "kpi_name": kpi_name,
        "name": kpi_name,
        "group": group,
        "definition": definition,
        "formula": formula,
        "unit": unit,
        "level": level,
        "visual_type": visual_type,
        "priority": priority,
        "dashboard_page": dashboard_page,
        "drilldown_behavior": drilldown_behavior,
        "drilldown_filter_preset": drilldown_filter_preset,
        "api_endpoint": api_endpoint,
    }


