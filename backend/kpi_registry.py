KPI_REGISTRY: List[Dict[str, Any]] = build_kpi_registry()
REGISTRY_BY_ID: Dict[str, Dict[str, Any]] = {r["kpi_id"]: r for r in KPI_REGISTRY}


def get_registry() -> List[Dict[str, Any]]:
    return KPI_REGISTRY


def get_kpi_entry(kpi_id: str) -> Dict[str, Any] | None:
    kid = (kpi_id or "").strip().upper()
    if kid and not kid.startswith("KPI-"):
        if kid.isdigit():
            kid = f"KPI-{int(kid):03d}"
    return REGISTRY_BY_ID.get(kid)
